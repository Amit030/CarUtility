
public abstract class Car{
	int id;
	String Model;
	double price;
	double reSaleValue;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getModel() {
		return Model;
	}
	public void setModel(String model) {
		Model = model;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public double getReSaleValue() {
		return reSaleValue;
	}
	public void setReSaleValue(double d) {
		this.reSaleValue = d;
	}
	
	
}
